package bbsanimal;

public class BbsAnimal {
	private int bbsAnimalID;
	private String bbsAnimalTitle;
	private String userID;
	private String bbsAnimalDate;
	private String bbsAnimalContent;
	private int bbsAnimalAvailable;
	private int bbsAnimalHit;
	
	public int getBbsAnimalID() {
		return bbsAnimalID;
	}
	public void setBbsAnimalID(int bbsAnimalID) {
		this.bbsAnimalID = bbsAnimalID;
	}
	public String getBbsAnimalTitle() {
		return bbsAnimalTitle;
	}
	public void setBbsAnimalTitle(String bbsAnimalTitle) {
		this.bbsAnimalTitle = bbsAnimalTitle;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getBbsAnimalDate() {
		return bbsAnimalDate;
	}
	public void setBbsAnimalDate(String bbsAnimalDate) {
		this.bbsAnimalDate = bbsAnimalDate;
	}
	public String getBbsAnimalContent() {
		return bbsAnimalContent;
	}
	public void setBbsAnimalContent(String bbsAnimalContent) {
		this.bbsAnimalContent = bbsAnimalContent;
	}
	public int getBbsAnimalAvailable() {
		return bbsAnimalAvailable;
	}
	public void setBbsAnimalAvailable(int bbsAnimalAvailable) {
		this.bbsAnimalAvailable = bbsAnimalAvailable;
	}
	public int getBbsAnimalHit() {
		return bbsAnimalHit;
	}
	public void setBbsAnimalHit(int bbsAnimalHit) {
		this.bbsAnimalHit = bbsAnimalHit;
	}

}
