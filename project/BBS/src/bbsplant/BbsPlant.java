package bbsplant;

public class BbsPlant {
	
	private int bbsPlantID;
	private String bbsPlantTitle;
	private String userID;
	private String bbsPlantDate;
	private String bbsPlantContent;
	private int bbsPlantAvailable;
	private int bbsPlantHit;
	
	public int getBbsPlantID() {
		return bbsPlantID;
	}
	public void setBbsPlantID(int bbsPlantID) {
		this.bbsPlantID = bbsPlantID;
	}
	public String getBbsPlantTitle() {
		return bbsPlantTitle;
	}
	public void setBbsPlantTitle(String bbsPlantTitle) {
		this.bbsPlantTitle = bbsPlantTitle;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getBbsPlantDate() {
		return bbsPlantDate;
	}
	public void setBbsPlantDate(String bbsPlantDate) {
		this.bbsPlantDate = bbsPlantDate;
	}
	public String getBbsPlantContent() {
		return bbsPlantContent;
	}
	public void setBbsPlantContent(String bbsPlantContent) {
		this.bbsPlantContent = bbsPlantContent;
	}
	public int getBbsPlantAvailable() {
		return bbsPlantAvailable;
	}
	public void setBbsPlantAvailable(int bbsPlantAvailable) {
		this.bbsPlantAvailable = bbsPlantAvailable;
	}
	public int getBbsPlantHit() {
		return bbsPlantHit;
	}
	public void setBbsPlantHit(int bbsPlantHit) {
		this.bbsPlantHit = bbsPlantHit;
	}
	
}
