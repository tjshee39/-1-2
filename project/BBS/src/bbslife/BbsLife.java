package bbslife;

public class BbsLife {
	
	private int bbsLifeID;
	private String bbsLifeTitle;
	private String userID;
	private String bbsLifeDate;
	private String bbsLifeContent;
	private int bbsLifeAvailable;
	private int bbsLifeHit;
	
	public int getBbsLifeID() {
		return bbsLifeID;
	}
	public void setBbsLifeID(int bbsLifeID) {
		this.bbsLifeID = bbsLifeID;
	}
	public String getBbsLifeTitle() {
		return bbsLifeTitle;
	}
	public void setBbsLifeTitle(String bbsLifeTitle) {
		this.bbsLifeTitle = bbsLifeTitle;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getBbsLifeDate() {
		return bbsLifeDate;
	}
	public void setBbsLifeDate(String bbsLifeDate) {
		this.bbsLifeDate = bbsLifeDate;
	}
	public String getBbsLifeContent() {
		return bbsLifeContent;
	}
	public void setBbsLifeContent(String bbsLifeContent) {
		this.bbsLifeContent = bbsLifeContent;
	}
	public int getBbsLifeAvailable() {
		return bbsLifeAvailable;
	}
	public void setBbsLifeAvailable(int bbsLifeAvailable) {
		this.bbsLifeAvailable = bbsLifeAvailable;
	}
	public int getBbsLifeHit() {
		return bbsLifeHit;
	}
	public void setBbsLifeHit(int bbsLifeHit) {
		this.bbsLifeHit = bbsLifeHit;
	}
	
}
